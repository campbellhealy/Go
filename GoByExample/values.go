package main

import "fmt"

func main() {

	// Concat two strings
	fmt.Println("go" + "lang")
	// Add two integers
	fmt.Println("1+1 =", 1+1)
	// divide two floats
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	//Booleans conditional AND 'if value one is same as value two = True otherwise False'
	fmt.Println(true && false)
	// Booleans conditional OR
	fmt.Println(true || false)
	// Boolean NOT ! opposite of the value
	fmt.Println(!true)
}
